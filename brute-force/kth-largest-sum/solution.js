/**
 * 문제: K번째 큰 수
 *
 * 현수는 1부터 100사이의 자연수가 적힌 N장의 카드를 가지고 있습니다.
 * 현수는 이 중 3장을 뽑아 각 카드에 적힌 수를 합한 값을 기록하려고 합니다.
 * 3장을 뽑을 수 있는 모든 경우를 기록합니다.
 * 기록한 값 중 K번째로 큰 수를 출력하는 프로그램을 작성하세요.
 *
 * 만약 큰 수부터 만들어진 수가 25 25 23 23 22 20 19.....이고 K값이 30이라면
 * K번째 큰 값은 22입니다.
 *
 * 입력설명:
 * - 첫 줄에 자연수 N(3≤N≤100)과 K(1≤K≤50) 입력되고,
 *   그 다음 줄에 N개의 카드값이 입력된다.
 *
 * 출력설명:
 * - 첫 줄에 K번째 수를 출력합니다.
 * - K번째 수가 존재하지 않으면 -1을 출력합니다.
 *
 * 입력예제:
 * n = 10, k = 3
 * cards = [13, 15, 34, 23, 45, 65, 33, 11, 26, 42]
 *
 * 출력예제:
 * 143
 *
 * 설명:
 * 가능한 3장의 합 중 상위 몇 개:
 * 65+45+42=152, 65+45+34=144, 65+45+33=143, 65+42+34=141, ...
 * 3번째로 큰 수는 143입니다.
 */

export function solution(n, k, cards) {
  let answer = 0;

  let tmp = new Set();

  for(let i = 0; i < n; i++) {
    for(let j = i + 1; j < n; j++) {
      for(let m = j + 1; m < n; m++) {
        tmp.add(cards[i] + cards[j] + cards[m])
      }
    }
  }

  let a = Array.from(tmp).sort((a, b) => b - a);
  
  if(k > a.length) {
    answer = -1;
  } else {
    answer = a[k - 1];
  }

  return answer;
}
