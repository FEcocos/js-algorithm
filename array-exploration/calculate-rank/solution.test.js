import { describe, test, expect } from "vitest";
import { solution } from "./solution.js";

describe("등수구하기", () => {
  test("테스트 케이스 1 - 기본 예제", () => {
    // 87점: 4등 (100, 92, 89보다 낮음)
    // 89점: 3등 (100, 92보다 낮음)
    // 92점: 2등 (100보다 낮음)
    // 100점: 1등 (가장 높음)
    // 76점: 5등 (가장 낮음)
    expect(solution([87, 89, 92, 100, 76])).toEqual([4, 3, 2, 1, 5]);
  });

  test("테스트 케이스 2 - 모두 같은 점수", () => {
    // 모두 90점이면 모두 1등
    expect(solution([90, 90, 90, 90])).toEqual([1, 1, 1, 1]);
  });

  test("테스트 케이스 3 - 같은 점수가 있는 경우", () => {
    // 100점: 1등
    // 90점: 2등 (첫번째 90점)
    // 90점: 2등 (두번째 90점)
    // 80점: 4등 (90점이 2명이므로 다음은 4등)
    expect(solution([100, 90, 90, 80])).toEqual([1, 2, 2, 4]);
  });

  test("테스트 케이스 4 - 3명이 같은 1등", () => {
    // 92점: 1등 (첫번째)
    // 92점: 1등 (두번째)
    // 92점: 1등 (세번째)
    // 90점: 4등 (92점이 3명이므로 다음은 4등)
    expect(solution([92, 92, 92, 90])).toEqual([1, 1, 1, 4]);
  });

  test("테스트 케이스 5 - 내림차순으로 정렬된 경우", () => {
    // 100: 1등, 90: 2등, 80: 3등, 70: 4등, 60: 5등
    expect(solution([100, 90, 80, 70, 60])).toEqual([1, 2, 3, 4, 5]);
  });

  test("테스트 케이스 6 - 오름차순으로 정렬된 경우", () => {
    // 60: 5등, 70: 4등, 80: 3등, 90: 2등, 100: 1등
    expect(solution([60, 70, 80, 90, 100])).toEqual([5, 4, 3, 2, 1]);
  });

  test("테스트 케이스 7 - 복잡한 케이스", () => {
    // 85점: 5등 (100, 95, 90, 90보다 낮음)
    // 90점: 3등 (100, 95보다 낮음)
    // 100점: 1등 (가장 높음)
    // 95점: 2등 (100보다 낮음)
    // 90점: 3등 (100, 95보다 낮음)
    // 75점: 6등 (가장 낮음)
    expect(solution([85, 90, 100, 95, 90, 75])).toEqual([5, 3, 1, 2, 3, 6]);
  });

  test("테스트 케이스 8 - 최소 인원", () => {
    // 100: 1등, 80: 2등, 90: 2등... 아니 잠깐
    // 100: 1등 (가장 높음)
    // 80: 3등 (100, 90보다 낮음)
    // 90: 2등 (100보다 낮음)
    expect(solution([100, 80, 90])).toEqual([1, 3, 2]);
  });
});

