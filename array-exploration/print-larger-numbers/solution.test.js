import { describe, test, expect } from "vitest";
import { solution } from "./solution.js";

describe("큰 수 출력하기", () => {
  test("테스트 케이스 1 - 기본 예제", () => {
    // 7: 첫번째(무조건), 3: 7>3(X), 9: 3<9(O), 5: 9>5(X), 6: 5<6(O), 12: 6<12(O)
    expect(solution([7, 3, 9, 5, 6, 12])).toEqual([7, 9, 6, 12]);
  });

  test("테스트 케이스 2 - 계속 증가하는 경우", () => {
    // 모든 수가 이전 수보다 크므로 모두 출력
    expect(solution([1, 2, 3, 4, 5])).toEqual([1, 2, 3, 4, 5]);
  });

  test("테스트 케이스 3 - 계속 감소하는 경우", () => {
    // 첫번째만 출력
    expect(solution([5, 4, 3, 2, 1])).toEqual([5]);
  });

  test("테스트 케이스 4 - 같은 수가 있는 경우", () => {
    // 10: 첫번째, 5: 10>5(X), 5: 5=5(X), 8: 5<8(O), 8: 8=8(X), 10: 8<10(O)
    expect(solution([10, 5, 5, 8, 8, 10])).toEqual([10, 8, 10]);
  });

  test("테스트 케이스 5 - 하나의 수", () => {
    // 첫번째 수는 무조건 출력
    expect(solution([100])).toEqual([100]);
  });

  test("테스트 케이스 6 - 복잡한 케이스", () => {
    // 3: 첫번째, 7: 3<7(O), 2: 7>2(X), 8: 2<8(O), 5: 8>5(X), 11: 5<11(O), 6: 11>6(X), 15: 6<15(O)
    expect(solution([3, 7, 2, 8, 5, 11, 6, 15])).toEqual([3, 7, 8, 11, 15]);
  });

  test("테스트 케이스 7 - 음수 포함", () => {
    // -5: 첫번째, -3: -5<-3(O), -7: -3>-7(X), 0: -7<0(O), 2: 0<2(O)
    expect(solution([-5, -3, -7, 0, 2])).toEqual([-5, -3, 0, 2]);
  });
});

