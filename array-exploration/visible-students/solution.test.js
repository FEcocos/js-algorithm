import { describe, test, expect } from "vitest";
import { solution } from "./solution.js";

describe("보이는 학생", () => {
  test("테스트 케이스 1 - 기본 예제", () => {
    // 130(첫번째,보임), 135(>130,보임), 148(>135,보임), 140(<148,안보임),
    // 145(<148,안보임), 150(>148,보임), 150(=150,안보임), 153(>150,보임)
    // 보이는 학생: 130, 135, 148, 150, 153 = 5명
    expect(solution([130, 135, 148, 140, 145, 150, 150, 153])).toBe(5);
  });

  test("테스트 케이스 2 - 모두 증가하는 경우", () => {
    // 모든 학생이 보임
    expect(solution([100, 110, 120, 130, 140])).toBe(5);
  });

  test("테스트 케이스 3 - 모두 감소하는 경우", () => {
    // 첫 번째 학생만 보임
    expect(solution([150, 140, 130, 120, 110])).toBe(1);
  });

  test("테스트 케이스 4 - 같은 키", () => {
    // 같은 키는 보이지 않음
    expect(solution([130, 130, 130, 130])).toBe(1);
  });

  test("테스트 케이스 5 - 한 명만", () => {
    // 첫 번째는 무조건 보임
    expect(solution([170])).toBe(1);
  });

  test("테스트 케이스 6 - 복잡한 케이스", () => {
    // 100(첫번째), 105(>100), 110(>105), 108(<110), 115(>110), 
    // 120(>115), 118(<120), 125(>120)
    // 보이는 학생: 100, 105, 110, 115, 120, 125 = 6명
    expect(solution([100, 105, 110, 108, 115, 120, 118, 125])).toBe(6);
  });

  test("테스트 케이스 7 - 중간에 큰 학생", () => {
    // 100(첫번째), 200(>100), 101(<200), 102(<200), 103(<200)
    // 보이는 학생: 100, 200 = 2명
    expect(solution([100, 200, 101, 102, 103])).toBe(2);
  });

  test("테스트 케이스 8 - 마지막이 가장 큼", () => {
    // 120(첫번째), 125(>120), 130(>125), 122(<130), 140(>130)
    // 보이는 학생: 120, 125, 130, 140 = 4명
    expect(solution([120, 125, 130, 122, 140])).toBe(4);
  });
});

