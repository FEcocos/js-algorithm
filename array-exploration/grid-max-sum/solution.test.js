import { describe, test, expect } from "vitest";
import { solution } from "./solution.js";

describe("격자판 최대합", () => {
  test("테스트 케이스 1 - 기본 예제", () => {
    const board = [
      [10, 13, 10, 12, 15],
      [12, 39, 30, 23, 11],
      [11, 25, 50, 53, 15],
      [19, 27, 29, 37, 27],
      [19, 13, 30, 13, 19]
    ];
    // 각 행의 합: 60, 115, 154, 139, 94
    // 각 열의 합: 71, 117, 149, 138, 87
    // 대각선1(좌상→우하): 10+39+50+37+19 = 155
    // 대각선2(우상→좌하): 15+23+50+27+19 = 134
    // 최대: 155
    expect(solution(board)).toBe(155);
  });

  test("테스트 케이스 2 - 3x3 격자판", () => {
    const board = [
      [1, 2, 3],
      [4, 5, 6],
      [7, 8, 9]
    ];
    // 행: 6, 15, 24
    // 열: 12, 15, 18
    // 대각선1: 1+5+9 = 15
    // 대각선2: 3+5+7 = 15
    // 최대: 24
    expect(solution(board)).toBe(24);
  });

  test("테스트 케이스 3 - 2x2 격자판", () => {
    const board = [
      [10, 20],
      [30, 40]
    ];
    // 행: 30, 70
    // 열: 40, 60
    // 대각선1: 10+40 = 50
    // 대각선2: 20+30 = 50
    // 최대: 70
    expect(solution(board)).toBe(70);
  });

  test("테스트 케이스 4 - 대각선이 최대인 경우", () => {
    const board = [
      [100, 1, 1],
      [1, 100, 1],
      [1, 1, 100]
    ];
    // 행: 102, 102, 102
    // 열: 102, 102, 102
    // 대각선1: 100+100+100 = 300
    // 대각선2: 1+100+1 = 102
    // 최대: 300
    expect(solution(board)).toBe(300);
  });

  test("테스트 케이스 5 - 열이 최대인 경우", () => {
    const board = [
      [1, 50, 1],
      [1, 50, 1],
      [1, 50, 1]
    ];
    // 행: 52, 52, 52
    // 열: 3, 150, 3
    // 대각선1: 1+50+1 = 52
    // 대각선2: 1+50+1 = 52
    // 최대: 150
    expect(solution(board)).toBe(150);
  });

  test("테스트 케이스 6 - 행이 최대인 경우", () => {
    const board = [
      [1, 1, 1],
      [50, 50, 50],
      [1, 1, 1]
    ];
    // 행: 3, 150, 3
    // 열: 52, 52, 52
    // 대각선1: 1+50+1 = 52
    // 대각선2: 1+50+1 = 52
    // 최대: 150
    expect(solution(board)).toBe(150);
  });

  test("테스트 케이스 7 - 4x4 격자판", () => {
    const board = [
      [10, 20, 30, 40],
      [15, 25, 35, 45],
      [20, 30, 40, 50],
      [25, 35, 45, 55]
    ];
    // 행: 100, 120, 140, 160
    // 열: 70, 110, 150, 190
    // 대각선1: 10+25+40+55 = 130
    // 대각선2: 40+35+30+25 = 130
    // 최대: 190
    expect(solution(board)).toBe(190);
  });
});

