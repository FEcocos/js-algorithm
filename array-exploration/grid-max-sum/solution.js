/**
 * 문제 제목: 격자판 최대합
 * 출처: 자바스크립트 알고리즘 문제풀이
 * 난이도: 배열탐색
 * 문제 링크:
 *
 * 문제 설명:
 * 5×5 격자판에 아래와 같이 숫자가 적혀있습니다.
 * N*N의 격자판이 주어지면 각 행의 합, 각 열의 합, 두 대각선의 합 중
 * 가장 큰 합을 출력합니다.
 *
 * 입력:
 * 첫 줄에 자연수 N이 주어진다.(1<=N<=50)
 * 두 번째 줄부터 N줄에 걸쳐 각 줄에 N개의 자연수가 주어진다.
 * 각 자연수는 100을 넘지 않는다.
 *
 * 출력:
 * 최대합을 출력합니다.
 *
 * 예제:
 * Input: [
 *   [10, 13, 10, 12, 15],
 *   [12, 39, 30, 23, 11],
 *   [11, 25, 50, 53, 15],
 *   [19, 27, 29, 37, 27],
 *   [19, 13, 30, 13, 19]
 * ]
 * Output: 155
 * (3행의 합: 11+25+50+53+15 = 154)
 * (3열의 합: 10+30+50+29+30 = 149)
 * (대각선: 10+39+50+37+19 = 155) ← 최대
 */

/**
 * 시간 복잡도: O()
 * 공간 복잡도: O()
 *
 * 접근 방법:
 * 1.
 * 2.
 * 3.
 */

export function solution(arr) {
  let answer = 0;
  let n = arr.length;
  let sum1 = 0;  // 대각선1 (좌상→우하)
  let sum2 = 0;  // 대각선2 (우상→좌하)

  for (let i = 0; i < n; i++) {
    let rowSum = 0;  // i번째 행의 합
    let colSum = 0;  // i번째 열의 합
    
    for (let j = 0; j < n; j++) {
      rowSum += arr[i][j];  // 행 합계
      colSum += arr[j][i];  // 열 합계
    }
    
    // 각 행과 열의 합 중 최대값 갱신
    answer = Math.max(answer, rowSum, colSum);
    
    // 대각선 합 계산
    sum1 += arr[i][i];
    sum2 += arr[i][n - i - 1];
  }

  // 대각선까지 포함한 최대값
  answer = Math.max(answer, sum1, sum2);

  return answer;
}

